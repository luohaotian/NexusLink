package com.SchoolBlog.model;

public class SchoolBean {
	private int school_id;
	private String school_name;
	private String school_img;
	private String school_info;
	public int getSchool_id() {
		return school_id;
	}
	public void setSchool_id(int school_id) {
		this.school_id = school_id;
	}
	public String getSchool_name() {
		return school_name;
	}
	public void setSchool_name(String school_name) {
		this.school_name = school_name;
	}
	public String getSchool_img() {
		return school_img;
	}
	public void setSchool_img(String school_img) {
		this.school_img = school_img;
	}
	public String getSchool_info() {
		return school_info;
	}
	public void setSchool_info(String school_info) {
		this.school_info = school_info;
	}
	
	

}
